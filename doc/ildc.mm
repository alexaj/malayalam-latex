Separate commands are provided for changing the fonts
inside any of the above options. For example,
if you are using `\texttt{karthika}' and want to
use `\texttt{sabari}' for some text, simply
use the command \verb"\sabari" before the text.
\verb"\revathi", \verb"\periyar", \verb"\kaumudi"  etc. also work.


